package com.startjava.lesson_1.variable;

public class Variable {
	public static void main(String[] args) {
		byte coreSum = 4;
		short ram = 6;
		int grafMem = 2;
		long sys = 64L;
		float cpuFreq = 1.6f;
		double freeRam = 5.48;
		char pavilion = '7';
		boolean notebook = true;
		System.out.println(coreSum);
		System.out.println(cpuFreq);
		System.out.println(ram);
		System.out.println(freeRam);
		System.out.println(grafMem);
		System.out.println(sys);
		System.out.println(pavilion);
		System.out.println(notebook);
	}
}